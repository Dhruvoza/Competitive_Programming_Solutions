import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		while(true)
		{
		    int temp = Integer.parseInt(br.readLine());
		    if(temp == 42)
		        break;
		    System.out.println(temp);
		}
	}
}
